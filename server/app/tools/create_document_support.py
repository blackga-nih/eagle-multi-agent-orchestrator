def _build_generation_prompt(doc_type: str, title: str, data: dict) -> str:
    """Build a structured prompt from session context data."""
    label = _DOC_TYPE_LABELS.get(doc_type, doc_type.replace("_", " ").title())
    parts = [f"Generate a {label} titled: {title}\n"]

    context_fields = {
        "description": "Requirement Description",
        "estimated_value": "Estimated Value",
        "period_of_performance": "Period of Performance",
        "security_requirements": "Security Requirements",
        "place_of_performance": "Place of Performance",
        "scope": "Scope Details",
        "deliverables": "Deliverables",
        "tasks": "Tasks",
        "competition": "Competition Strategy",
        "contract_type": "Contract Type",
        "set_aside": "Set-Aside",
        "naics_code": "NAICS Code",
        "authority": "Authority Cited",
        "contractor": "Contractor",
        "rationale": "Rationale",
        "evaluation_method": "Evaluation Method",
        "factors": "Evaluation Factors",
        "impact_level": "Impact Level",
        "cloud_services": "Cloud Services Involved",
        "nominee_name": "COR Nominee Name",
        "nominee_title": "COR Nominee Title",
        "nominee_org": "COR Nominee Organization",
        "nominee_email": "COR Nominee Email",
        "fac_cor_level": "FAC-COR Level",
        "contract_number": "Contract Number",
        "overhead_rate": "Overhead Rate (%)",
        "contingency_rate": "Contingency Rate (%)",
        "risk_to_government": "Risk to Government",
        "risk_to_contractor": "Risk to Contractor",
        "funding_by_fy": "Funding by Fiscal Year",
    }

    for key, label_text in context_fields.items():
        value = data.get(key)
        if value is None or value == "":
            continue
        if isinstance(value, list):
            if value and isinstance(value[0], dict):
                value = _json.dumps(value, default=str)
            else:
                value = "\n".join(f"- {item}" for item in value)
        elif isinstance(value, bool):
            value = "Yes" if value else "No"
        parts.append(f"**{label_text}:** {value}")

    # Add line items for IGCE
    line_items = data.get("line_items")
    if line_items:
        parts.append(f"**Line Items:** {_json.dumps(line_items, default=str)}")

    # Add conversation history as context (NOT to be pasted in)
    conv_history = data.get("conversation_history", [])
    if conv_history:
        parts.append(
            "\n## Conversation Context (for understanding the requirement only — do NOT paste into document)"
        )
        for msg in conv_history[-8:]:
            role = msg.get("role", "user").upper()
            text = str(msg.get("text", ""))[:500]
            parts.append(f"[{role}]: {text}")

    return "\n\n".join(parts)


def _minimal_skeleton(doc_type: str, title: str) -> str:
    """Honest fallback when Bedrock is unavailable."""
    label = _DOC_TYPE_LABELS.get(doc_type, doc_type.replace("_", " ").title())
    return f"""# {label.upper()}
## {title}
### National Cancer Institute (NCI)

**Document Status:** DRAFT — Generated {time.strftime("%Y-%m-%d %H:%M UTC")}

---

> **Note:** This document could not be generated because the AI service is temporarily
> unavailable. Please try again, or use the chat to request document generation
> (e.g., "Generate a {label} for this acquisition").

---
*This document was generated by EAGLE — NCI Acquisition Assistant*
"""


def _llm_generate_document(doc_type: str, title: str, data: dict) -> str:
    """Generate acquisition document content via Bedrock LLM call.

    Falls back to minimal skeleton if Bedrock is unavailable.
    """
    system_prompt = _DOC_TYPE_SYSTEM_PROMPTS.get(doc_type)
    if not system_prompt:
        logger.warning("No system prompt for doc_type=%s, using skeleton", doc_type)
        return _minimal_skeleton(doc_type, title)

    user_message = _build_generation_prompt(doc_type, title, data)

    try:
        bedrock = _get_doc_gen_bedrock()
        response = bedrock.converse(
            modelId=_DOC_GEN_MODEL,
            system=[{"text": system_prompt}],
            messages=[{"role": "user", "content": [{"text": user_message}]}],
            inferenceConfig={"maxTokens": 8192, "temperature": 0.3},
        )
        content = response["output"]["message"]["content"][0]["text"]
        return content.strip()
    except Exception as exc:
        logger.warning("Bedrock doc generation failed for %s: %s", doc_type, exc)
        return _minimal_skeleton(doc_type, title)


def _normalize_context_text(text: str) -> str:
    cleaned = (text or "").strip()
    if not cleaned:
        return ""
    marker = "[USER REQUEST]"
    if marker in cleaned:
        tail = cleaned.split(marker, 1)[1].strip()
        if "Instruction:" in tail:
            tail = tail.split("Instruction:", 1)[0].strip()
        if tail:
            cleaned = tail
    return cleaned


def _load_recent_user_context(session_id: str | None = None) -> list[str]:
    leaf_session_id = extract_leaf_session_id(session_id)
    if not leaf_session_id:
        return []

    tenant_id = extract_tenant_id(session_id)
    user_id = extract_user_id(session_id)

    try:
        from ..session_store import get_messages

        messages = get_messages(leaf_session_id, tenant_id, user_id, limit=30)
        context_texts: list[str] = []
        for msg in messages:
            role = msg.get("role", "")
            if role not in ("user", "assistant"):
                continue

            content = msg.get("content", "")
            text = ""
            if isinstance(content, str):
                text = _normalize_context_text(content)
            elif isinstance(content, list):
                text_parts: list[str] = []
                for block in content:
                    if isinstance(block, dict):
                        block_text = block.get("text")
                        if isinstance(block_text, str) and block_text.strip():
                            text_parts.append(block_text.strip())
                if text_parts:
                    text = _normalize_context_text("\n".join(text_parts))

            if text:
                prefix = "" if role == "user" else "[ASSISTANT] "
                context_texts.append(prefix + text)

        return context_texts[-12:]
    except Exception as exc:
        logger.debug("Could not load session context for create_document: %s", exc)
        return []


def _extract_first_money_value(text: str) -> str | None:
    if not text:
        return None

    match = re.search(r"\$[0-9][0-9,]*(?:\.[0-9]+)?", text)
    if match:
        return match.group(0)

    match = re.search(
        r"\b[0-9][0-9,]*(?:\.[0-9]+)?\s*(?:million|billion|k|m)\b",
        text,
        flags=re.IGNORECASE,
    )
    if match:
        return match.group(0)
    return None


def _extract_period(text: str) -> str | None:
    if not text:
        return None
    match = re.search(
        r"\b\d+\s*(?:month|months|year|years)\b(?:[^.,;\n]{0,40})",
        text,
        flags=re.IGNORECASE,
    )
    if match:
        return match.group(0).strip()
    return None


_GENERATION_TRIGGER_RE = re.compile(
    r"^(generate|create|draft|write|produce|prepare|build|make)\s+"
    r"(the|a|an|my|our)?\s*"
    r"(statement of work|sow|igce|ige|cost estimate|market research|"
    r"acquisition plan|justification|document|report)",
    re.IGNORECASE,
)


def _is_generation_trigger(text: str) -> bool:
    t = text.strip()
    if len(t) > 200:
        return False
    return bool(_GENERATION_TRIGGER_RE.search(t))


def _extract_section_bullets(text: str) -> dict[str, list[str]]:
    sections: dict[str, list[str]] = {}
    current: str | None = None
    aliases = {
        "project description": "project_description",
        "technical requirements": "technical_requirements",
        "scope of work": "scope_of_work",
        "deliverables": "deliverables",
        "environment tiers": "environment_tiers",
        "security": "security",
    }

    for raw_line in (text or "").splitlines():
        line = raw_line.strip()
        if not line:
            continue

        heading_key = line.rstrip(":").strip().lower()
        if heading_key in aliases:
            current = aliases[heading_key]
            sections.setdefault(current, [])
            continue

        if line.startswith("- ") or line.startswith("* "):
            item = line[2:].strip().strip('"')
            if item:
                sections.setdefault(current or "general", []).append(item)

    return sections


def _augment_document_data_from_context(
    doc_type: str,
    title: str,
    data: dict | None,
    session_id: str | None,
) -> dict:
    merged = dict(data or {})
    context_messages = _load_recent_user_context(session_id)
    if not context_messages:
        return merged

    user_msgs = [m for m in context_messages if not m.startswith("[ASSISTANT] ")]
    assistant_msgs = [
        m.removeprefix("[ASSISTANT] ")
        for m in context_messages
        if m.startswith("[ASSISTANT] ")
    ]
    substantive_user_msgs = [m for m in user_msgs if not _is_generation_trigger(m)]
    last_user_text = (substantive_user_msgs or user_msgs)[-1] if user_msgs else ""
    context_blob = " ".join(context_messages[-8:])

    requirement = (last_user_text or context_blob).strip()
    if requirement:
        requirement = requirement[:500]

    money = _extract_first_money_value(context_blob)
    period = _extract_period(context_blob)

    if requirement:
        merged.setdefault("description", requirement)
        merged.setdefault("requirement", requirement)
        merged.setdefault("objective", requirement)
    if money:
        merged.setdefault("estimated_cost", money)
        merged.setdefault("estimated_value", money)
        merged.setdefault("budget", money)
        merged.setdefault("total_estimate", money)
    if period:
        merged.setdefault("period_of_performance", period)
        merged.setdefault("timeline", period)

    parsed_sections = _extract_section_bullets("\n".join(context_messages[-3:]))
    project_description = " ".join(
        parsed_sections.get("project_description", [])
    ).strip()
    if project_description:
        existing_desc = str(merged.get("description", "")).strip()
        if (
            not existing_desc
            or "project description" in existing_desc.lower()
            or len(existing_desc) > 320
        ):
            merged["description"] = project_description[:500]
        merged.setdefault("requirement", project_description[:500])
        merged.setdefault("objective", project_description[:500])
    if parsed_sections.get("deliverables"):
        merged.setdefault("deliverables", parsed_sections["deliverables"][:15])
    if parsed_sections.get("security"):
        merged.setdefault(
            "security_requirements", "; ".join(parsed_sections["security"])[:600]
        )
    if parsed_sections.get("environment_tiers"):
        merged.setdefault(
            "place_of_performance",
            "; ".join(parsed_sections["environment_tiers"])[:300],
        )

    if doc_type == "igce":
        merged.setdefault("item_name", title or "Primary acquisition item")
    if doc_type == "market_research":
        merged.setdefault("requirement_summary", requirement or title)
    if doc_type == "sow":
        scope_items = parsed_sections.get("scope_of_work", [])
        tech_items = parsed_sections.get("technical_requirements", [])
        combined_tasks = (scope_items + tech_items)[:20]
        if combined_tasks:
            merged.setdefault("tasks", combined_tasks)
        if scope_items:
            merged.setdefault(
                "scope", "\n".join(f"- {item}" for item in scope_items[:10])
            )
        elif substantive_user_msgs:
            merged.setdefault("scope", max(substantive_user_msgs[-6:], key=len)[:500])

    if assistant_msgs:
        clean_assistant = [
            msg
            for msg in assistant_msgs
            if not re.search(
                r"Your task is to create a detailed summary|"
                r"You are an? .{0,30}(?:assistant|agent|specialist)|"
                r"SYSTEM PROMPT|"
                r"<instructions>",
                msg[:200],
                re.IGNORECASE,
            )
        ]
        source = clean_assistant or assistant_msgs
        merged.setdefault("conversation_context", max(source[-4:], key=len)[:3000])

    all_msgs = []
    for message in context_messages:
        if message.startswith("[ASSISTANT] "):
            all_msgs.append({"role": "assistant", "text": message[12:]})
        else:
            all_msgs.append({"role": "user", "text": message})
    if all_msgs:
        merged.setdefault("conversation_history", all_msgs)

    return merged


