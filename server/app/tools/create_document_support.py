_SOW_SECTION_HINTS: dict[str, str] = {
    "1": "background and purpose",
    "2": "scope",
    "3": "period of performance",
    "4": "applicable documents and standards",
    "5": "tasks and requirements",
    "6": "deliverables",
    "7": "government-furnished property",
    "8": "quality assurance surveillance plan",
    "9": "place of performance",
    "10": "security requirements",
}


def _extract_sow_clear_targets(edit_request: str) -> list[str]:
    req = (edit_request or "").strip().lower()
    if not req:
        return []
    clear_intent = (
        "clear" in req or "blank" in req or "to be completed" in req or "remove" in req
    )
    if not clear_intent:
        return []

    targets: list[str] = []
    for section_num in re.findall(r"\bsection\s*(\d{1,2})\b", req):
        hint = _SOW_SECTION_HINTS.get(section_num)
        if hint and hint not in targets:
            targets.append(hint)

    for alias in (
        "background and purpose",
        "scope",
        "period of performance",
        "applicable documents and standards",
        "tasks and requirements",
        "deliverables",
        "government-furnished property",
        "quality assurance surveillance plan",
        "place of performance",
        "security requirements",
    ):
        if alias in req and alias not in targets:
            targets.append(alias)

    if "scope" in req and "scope" not in targets:
        targets.append("scope")
    return targets


def _apply_sow_clear_edits(current_content: str, edit_request: str) -> str | None:
    if not isinstance(current_content, str) or not current_content.strip():
        return None

    targets = _extract_sow_clear_targets(edit_request)
    if not targets:
        return None

    heading_matches = list(re.finditer(r"(?m)^##\s+(.+?)\s*$", current_content))
    if not heading_matches:
        return None

    rebuilt: list[str] = []
    cursor = 0
    changed = False

    for idx, heading_match in enumerate(heading_matches):
        heading_end = heading_match.end()
        next_start = (
            heading_matches[idx + 1].start()
            if idx + 1 < len(heading_matches)
            else len(current_content)
        )
        heading_text = heading_match.group(1).strip().lower()
        body_text = current_content[heading_end:next_start]
        rebuilt.append(current_content[cursor:heading_end])

        if any(target in heading_text for target in targets):
            if body_text.strip() != "[To be completed]":
                changed = True
            rebuilt.append("\n\n[To be completed]\n\n")
        else:
            rebuilt.append(body_text)
        cursor = next_start

    updated = "".join(rebuilt)
    return updated if changed else None


def _update_document_content(
    tenant_id: str,
    doc_key: str,
    content: str,
    change_source: str = "ai_edit",
    session_id: str | None = None,
) -> dict:
    if not doc_key:
        return {"error": "update_existing_key is required but empty"}
    if not content:
        return {
            "error": "content is required for update. Provide the full document markdown in the content parameter."
        }

    user_id = extract_user_id(session_id)
    bucket = os.getenv("S3_BUCKET", "eagle-documents-695681773636-dev")

    if not is_allowed_document_key(doc_key, tenant_id, user_id):
        return {"error": "Access denied: document key is not within an allowed scope"}

    if "/packages/" in doc_key:
        from ..document_service import create_package_document_version

        parts = doc_key.split("/")
        try:
            pkg_idx = parts.index("packages")
            package_id = parts[pkg_idx + 1]
            filename = parts[-1]
            doc_type = (
                filename.split("_v")[0]
                if "_v" in filename
                else filename.rsplit(".", 1)[0]
            )
            title = doc_type.replace("_", " ").title()
        except (ValueError, IndexError):
            return {"error": "Invalid package document key format"}

        result = create_package_document_version(
            tenant_id=tenant_id,
            package_id=package_id,
            doc_type=doc_type,
            content=content,
            title=title,
            file_type="md",
            created_by_user_id=user_id,
            session_id=session_id,
            change_source=change_source,
        )
        if not result.success:
            return {"error": result.error or "Failed to create document version"}
        return {
            "success": True,
            "mode": "update_package",
            "package_id": package_id,
            "doc_type": doc_type,
            "document_type": doc_type,
            "title": title,
            "s3_key": result.s3_key,
            "key": result.s3_key,
            "version": result.version,
            "document_id": result.document_id,
            "status": "draft",
            "content": content,
            "word_count": len(content.split()),
            "message": f"Document updated (version {result.version})",
        }

    try:
        s3 = get_s3()
        s3.put_object(
            Bucket=bucket,
            Key=doc_key,
            Body=content.encode("utf-8"),
            ContentType="text/markdown; charset=utf-8",
        )
        from ..changelog_store import write_document_changelog_entry

        write_document_changelog_entry(
            tenant_id=tenant_id,
            document_key=doc_key,
            change_type="update",
            change_source=change_source,
            change_summary="Updated document content",
            actor_user_id=user_id,
            session_id=session_id,
        )
        # Extract doc_type from key filename (e.g., "sow_20260325.md" → "sow")
        ws_filename = doc_key.rsplit("/", 1)[-1] if "/" in doc_key else doc_key
        ws_doc_type = ws_filename.split("_", 1)[0] if "_" in ws_filename else ws_filename.rsplit(".", 1)[0]
        return {
            "success": True,
            "mode": "update_workspace",
            "s3_key": doc_key,
            "key": doc_key,
            "doc_type": ws_doc_type,
            "document_type": ws_doc_type,
            "title": ws_doc_type.replace("_", " ").title(),
            "content": content,
            "word_count": len(content.split()),
            "message": "Document updated",
        }
    except Exception as exc:
        logger.warning("Failed to update workspace document %s: %s", doc_key, exc)
        return {"error": f"Failed to update document: {exc}"}


# ── Provenance metadata injection ────────────────────────────────────

_EAGLE_FOOTER_RE = re.compile(
    r"\n---\s*\n\s*\*{1,2}This document was generated by EAGLE[^*]*\*{1,2}\s*(?:\n---\s*)?$"
)


def _append_provenance_metadata(
    content: str,
    provenance: dict | None,
    source: str,
    generated_at: str,
) -> str:
    """Append a Document Metadata section to generated content, replacing the bare footer."""
    if "## Document Metadata" in content:
        return content  # double-injection guard

    # Strip old bare footer if present
    content = _EAGLE_FOOTER_RE.sub("", content)

    # Build metadata table rows
    rows = [
        ("Generated by", "EAGLE — NCI Acquisition Assistant"),
    ]
    if provenance:
        rows.append(("Template", provenance.get("template_id", "N/A")))
        rows.append(("Template Source", provenance.get("template_source", source)))
        rows.append(("Template Version", f"v{provenance.get('template_version', 1)}"))
    else:
        rows.append(("Template", "Built-in markdown generator"))
        rows.append(("Template Source", source or "markdown_fallback"))
    rows.append(
        ("Document Type", provenance.get("doc_type", "N/A") if provenance else "N/A")
    )
    rows.append(("Generated", generated_at))

    table_lines = ["| Field | Value |", "|-------|-------|"]
    for field, value in rows:
        table_lines.append(f"| **{field}** | {value} |")

    metadata_section = "\n".join(
        [
            "",
            "---",
            "",
            "## Document Metadata",
            "",
            *table_lines,
            "",
            "*This is a draft document. Review and customize before official use.*",
        ]
    )

    return content.rstrip() + "\n" + metadata_section + "\n"


