# T1 — Metadata section present in all doc types
# ══════════════════════════════════════════════════════════════════════

class TestProvenanceMetadata:
    """Verifies the Document Metadata section is required in all documents."""

    @pytest.mark.parametrize("doc_type", ALL_DOC_TYPES)
    def test_metadata_section_present_all_doc_types(self, doc_type, mock_s3_client):
        """Every doc type must include a Document Metadata section."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            with patch("app.agentic_service._get_s3", return_value=mock_s3_client):
                from app.agentic_service import _exec_create_document
                result = _exec_create_document(
                    {"doc_type": doc_type, "title": f"Test {doc_type}"},
                    tenant_id="test-tenant",
                    session_id="ses-prov-001",
                )
        content = result["content"]
        assert "## Document Metadata" in content, (
            f"{doc_type}: missing '## Document Metadata' section"
        )
        assert "Generated by" in content, (
            f"{doc_type}: missing 'Generated by' row"
        )
        assert "Template" in content, (
            f"{doc_type}: missing 'Template' row"
        )

    def test_metadata_contains_template_info(self, mock_s3_client):
        """When template_id is provided, it appears in the metadata."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            with patch("app.agentic_service._get_s3", return_value=mock_s3_client):
                from app.agentic_service import _exec_create_document
                result = _exec_create_document(
                    {
                        "doc_type": "sow",
                        "title": "SOW with Template",
                        "template_id": "essential-templates/sow-template-v2.docx",
                    },
                    tenant_id="test-tenant",
                    session_id="ses-prov-002",
                )
        content = result["content"]
        # The resolved template path may differ from the input template_id
        # (e.g. "sow-template-v2.docx" resolves to "statement-of-work-template-eagle-v2.docx")
        assert "essential-templates/" in content, (
            "template_id not cited in metadata"
        )
        assert "Template Source" in content

    def test_metadata_graceful_without_provenance(self, mock_s3_client):
        """Markdown fallback docs still get metadata with fallback values."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            with patch("app.agentic_service._get_s3", return_value=mock_s3_client):
                from app.agentic_service import _exec_create_document
                result = _exec_create_document(
                    {"doc_type": "eval_criteria", "title": "Eval Criteria"},
                    tenant_id="test-tenant",
                    session_id="ses-prov-003",
                )
        content = result["content"]
        assert "## Document Metadata" in content
        assert "Built-in markdown generator" in content, (
            "fallback template label missing"
        )

    def test_old_footer_replaced(self, mock_s3_client):
        """The old bare EAGLE footer must not appear outside the metadata section."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            with patch("app.agentic_service._get_s3", return_value=mock_s3_client):
                from app.agentic_service import _exec_create_document
                result = _exec_create_document(
                    {"doc_type": "sow", "title": "Footer Test"},
                    tenant_id="test-tenant",
                    session_id="ses-prov-004",
                )
        content = result["content"]
        # Split at the metadata heading
        parts = content.split("## Document Metadata")
        pre_metadata = parts[0]
        # The old footer should NOT appear before the metadata section
        assert "*This document was generated by EAGLE" not in pre_metadata, (
            "Old bare footer still present above metadata section"
        )

    def test_ai_content_gets_metadata(self, mock_s3_client):
        """AI-provided content also gets the metadata section appended."""
        ai_content = (
            "# My Custom SOW\n\nThis is AI-generated content.\n\n"
            "---\n*This document was generated by EAGLE \u2014 NCI Acquisition Assistant*"
        )
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            with patch("app.agentic_service._get_s3", return_value=mock_s3_client):
                from app.agentic_service import _exec_create_document
                result = _exec_create_document(
                    {
                        "doc_type": "sow",
                        "title": "AI SOW",
                        "content": ai_content,
                    },
                    tenant_id="test-tenant",
                    session_id="ses-prov-005",
                )
        content = result["content"]
        assert "## Document Metadata" in content
        # Old footer should be replaced
        pre_metadata = content.split("## Document Metadata")[0]
        assert "*This document was generated by EAGLE" not in pre_metadata


