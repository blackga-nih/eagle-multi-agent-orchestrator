class TestAppendProvenanceMetadataHelper:
    """Direct unit tests for _append_provenance_metadata()."""

    def test_double_injection_guard(self):
        """Content with existing metadata section is returned unchanged."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            from app.agentic_service import _append_provenance_metadata
            content = "# Doc\n\n## Document Metadata\n\n| Field | Value |\n"
            result = _append_provenance_metadata(content, None, "test", "2026-03-25")
            assert result == content

    def test_metadata_renders_in_docx(self):
        """Metadata section markdown renders through markdown_to_docx without errors."""
        with patch.dict(os.environ, ENV_PATCH, clear=False):
            from app.agentic_service import _append_provenance_metadata
            from app.document_export import markdown_to_docx

            content = "# Test Document\n\nSome content here.\n"
            provenance = {
                "template_id": "sow-template-v2.docx",
                "template_source": "s3_template",
                "template_version": 1,
                "doc_type": "sow",
            }
            enriched = _append_provenance_metadata(
                content, provenance, "s3_template", "2026-03-25 14:30 UTC"
            )
            # Should not raise
            docx_bytes = markdown_to_docx(enriched, title="Test Document")
            assert len(docx_bytes) > 0

            # Verify the DOCX contains the metadata heading
            from io import BytesIO
            from docx import Document
            doc = Document(BytesIO(docx_bytes))
            headings = [p.text for p in doc.paragraphs if p.style.name.startswith("Heading")]
            assert any("Document Metadata" in h for h in headings), (
                f"'Document Metadata' heading not found in DOCX. Headings: {headings}"
            )
